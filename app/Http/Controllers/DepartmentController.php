    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=>'required' 
        ]);

        $depart = new DtbDepartment();
        $depart->name = $request->name;
        $results = $depart->save();

        if($results) {
            return redirect('/department')->with('message-success', 'New department has been added');
        } else {
            return redirect('/department')->with('message-danger', 'Something went wrong');
        }
    }

